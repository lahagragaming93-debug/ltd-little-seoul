/**
 * Retourne le markup SVG inline d'une icone.
 * @param {string} name  cle de l'icone (cf. PATHS)
 * @param {object} [opts]
 * @param {number} [opts.size=20]   taille en px (width = height)
 * @param {string} [opts.cls='']    classe(s) CSS additionnelle(s)
 * @param {number} [opts.stroke=2]  epaisseur du trait
 * @param {string} [opts.title]     <title> accessible (tooltip natif)
 * @returns {string} chaine HTML d'un <svg>
 */
export function icon(name, opts = {}) {
  const { size = 20, cls = '', stroke = 2, title } = opts;
  const body = PATHS[name] || PATHS['dot'];
  const cls2 = ('ico' + (cls ? ' ' + cls : '')).trim();
  const titleEl = title ? `<title>${String(title).replace(/[<>&]/g, '')}</title>` : '';
  return `<svg class="${cls2}" width="${size}" height="${size}" viewBox="0 0 24 24" `
    + `fill="none" stroke="currentColor" stroke-width="${stroke}" `
    + `stroke-linecap="round" stroke-linejoin="round" aria-hidden="true" focusable="false">`
    + `${titleEl}${body}</svg>`;
}

/** True si une icone existe pour ce nom. */
export function hasIcon(name) {
  return Object.prototype.hasOwnProperty.call(PATHS, name);
}
